function stringToArray(string){
  let element = '';
  let array1 = [];
  for (let i = 0; i < string.length; i++){
    if (string[i] != ' '){
      element += string[i];
    }else{
      array1.push(element);
      element = '';
    }
    if (i == string.length -1){
      array1.push(element);
    }
  }
  return array1;
}
