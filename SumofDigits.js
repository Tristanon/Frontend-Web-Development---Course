function digitalRoot(n) {
  lst = n.toString().split('');
  Sum = 0;
  while (true){
    for (let i = 0; i < lst.length; i++){
      Sum = Sum + parseInt(lst[i]);
    }
    if ( Sum < 10){
      return Sum;
    }else{
      lst = Sum.toString().split('');
      Sum = 0;
    }
  }
}
