function validatePIN (pin) {
  count = 0;
  for (let i = 0; i < pin.length; i++){
    count ++;
    if(/[0-9]/.test(pin[i]) == false){
      return false;
    }
  }
  if (pin.length == 4 || pin.length == 6){
    if (count == pin.length){
      return true;
    }else{
      return false;
    }
  }else{
    return false;
  }
}
