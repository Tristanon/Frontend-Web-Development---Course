function list(names){
  result = ''
  for (let i =0; i < names.length; i++ ){
    if ( i == 0){
      result = names[i].name;
    }else if ( i == names.length - 1){
      result = result + ' & ' + names[i].name;
    }else{
      result = result + ', ' + names[i].name;
    }
  }
  return result;
}
